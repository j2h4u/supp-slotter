def review_context_key(warning: dict) -> str | None:
    concern = str(warning.get("concern") or "")
    category = str(warning.get("category") or "")
    action = str(warning.get("action") or "")
    text = " ".join([concern, category, action]).lower()

    if "bleeding" in text or "fibrinolytic" in text or "antiplatelet" in text:
        return "bleeding_context"
    if "blood-pressure" in text or "blood pressure" in text or "hypotension" in text:
        return "blood_pressure"
    if "inside one product" in text or "intra-product" in text:
        return "intra_product_conflicts"
    if "missing balance" in text or "missing support" in text or "paired" in text:
        return "missing_pairings"
    if "narrow therapeutic window" in text or "narrow-window" in text:
        return "narrow_window_minerals"
    if "potassium" in text or "hyperkalemia" in text:
        return "potassium_medication"
    if "timing conflict" in text:
        return "timing_conflicts"
    if "unmatched" in text or "unresolved active concern" in text:
        return "unmatched_concerns"
    return None


def warning_subject(warning: dict) -> str:
    for key in ("product", "substance", "source", "target"):
        value = warning.get(key)
        if isinstance(value, str) and value:
            return value
    return "Stack"


def build_review_contexts(warnings: list[dict]) -> list[dict]:
    grouped: dict[str, dict[str, set[str]]] = {}
    for warning in warnings:
        key = review_context_key(warning)
        if key is None:
            continue
        context = grouped.setdefault(key, {"items": set(), "actions": set()})
        context["items"].add(warning_subject(warning))
        action = warning.get("action")
        if isinstance(action, str) and action:
            context["actions"].add(action)

    return [
        {
            "context": REVIEW_CONTEXTS.get(key, key.replace("_", " ").title()),
            "items": sorted(value["items"], key=str.casefold),
            "actions": sorted(value["actions"], key=str.casefold),
        }
        for key, value in sorted(grouped.items())
    ]


def humanize_warning(
    warning: dict,
    *,
    products: dict[str, dict],
    substances: dict[str, dict],
) -> dict:
    warning_type = str(warning.get("type") or "review")
    trait = str(warning.get("trait") or "")
    relation = str(warning.get("relation") or "")
    product_id = warning.get("product")
    product = (
        format_product_name(products.get(product_id) or {"id": product_id})
        if isinstance(product_id, str)
        else None
    )
    out: dict = {
        "category": WARNING_CATEGORY_LABELS.get(
            warning_type,
            "Review",
        )
    }
    if product:
        out["product"] = product

    substance_id = warning.get("substance")
    if isinstance(substance_id, str):
        out["substance"] = format_substance_name(
            substances.get(substance_id) or {"id": substance_id}
        )

    source_id = warning.get("source_substance")
    target_id = warning.get("target_substance")
    if isinstance(source_id, str):
        out["source"] = (
            format_substance_name(substances[source_id])
            if source_id in substances
            else str(warning.get("source_name") or source_id)
        )
    if isinstance(target_id, str):
        out["target"] = (
            format_substance_name(substances[target_id])
            if target_id in substances
            else str(warning.get("target_name") or target_id)
        )

    if trait:
        out["concern"] = trait.split(":", 1)[1].replace("_", " ")
    elif relation:
        out["concern"] = relation.replace("_", " ")
    elif warning_type.startswith("missing_"):
        out["concern"] = warning_type.replace("_", " ")
    else:
        out["concern"] = warning_type.replace("_", " ")

    message = warning.get("message") or warning.get("reason")
    if isinstance(message, str) and message:
        if "operator attention" not in message:
            out["note"] = message
    action = warning.get("action")
    out["action"] = (
        action if isinstance(action, str) and action else warning_action(warning_type, trait, relation)
    )
    return out


def is_generic_manual_review_warning(warning: dict) -> bool:
    return warning.get("trait") == "risk:manual_review"


def build_action_points(warnings: list[dict]) -> list[str]:
    subjects_by_action: dict[str, set[str]] = {}
    for warning in warnings:
        concern = warning.get("concern")
        if concern == "manual review":
            continue
        product = warning.get("product")
        substance = warning.get("substance") or warning.get("source")
        action = warning.get("action")
        if not isinstance(action, str):
            continue
        if warning.get("category") == "Unresolved active concern":
            subject = "Unresolved active concerns"
        else:
            subject = product or substance or "Stack"
        subjects_by_action.setdefault(action, set()).add(str(subject))

    points: list[str] = []
    for action, subjects in subjects_by_action.items():
        subject_list = sorted(subjects, key=str.casefold)
        if len(subject_list) == 1:
            points.append(f"{subject_list[0]}: {action}")
        else:
            points.append(f"{'; '.join(subject_list)}: {action}")
    return points[:8]


def collect_active_unmatched_concerns(
    *,
    active_order: list[str],
    active_components: dict[str, list[str]],
    item_products: dict[str, str],
    products: dict[str, dict],
    substances: dict[str, dict],
) -> list[dict]:
    warnings: list[dict] = []
    seen: set[tuple[str, str, str]] = set()
    for item_id in active_order:
        product_id = item_products[item_id]
        product = products.get(product_id) or {}
        for concern in product.get("unmatched_concerns") or []:
            if not isinstance(concern, str):
                continue
            key = ("product", product_id, concern)
            if key in seen:
                continue
            seen.add(key)
            warnings.append(
                {
                    "type": "unmatched_concern",
                    "item": item_id,
                    "product": product_id,
                    "message": concern,
                }
            )
        for substance_id in active_components[item_id]:
            substance = substances.get(substance_id) or {}
            for concern in substance.get("unmatched_concerns") or []:
                if not isinstance(concern, str):
                    continue
                key = ("substance", substance_id, concern)
                if key in seen:
                    continue
                seen.add(key)
                warnings.append(
                    {
                        "type": "unmatched_concern",
                        "item": item_id,
                        "product": product_id,
                        "substance": substance_id,
                        "message": concern,
                    }
                )
    return warnings


def build_placement_notes(schedule: dict) -> list[dict]:
    notes: list[dict] = []
    for product_name, explanation in schedule.get("explanations", {}).items():
        why_here = [
            note
            for note in explanation.get("why_here", [])
            if isinstance(note, str) and "tradeoff" in note.lower()
        ]
        if not why_here:
            continue
        notes.append(
            {
                "product": product_name,
                "pillbox": explanation.get("pillbox"),
                "slot": explanation.get("slot"),
                "notes": why_here,
            }
        )
    return sorted(notes, key=lambda entry: str(entry["product"]).casefold())


def build_schedule_summary(schedule: dict) -> dict:
    take: dict[str, list[str]] = {}
    for pillbox_name, pillbox in schedule.get("pillboxes", {}).items():
        lines: list[str] = []
        for slot in pillbox.get("slots", {}).values():
            if not slot.get("products"):
                continue
            lines.append(f"{slot['label']}: {', '.join(slot['products'])}")
        if lines:
            take[pillbox_name] = lines
    return {"take": take}


def collect_goal_substance_refs(goal_files: list[Path]) -> set[str]:
    refs: set[str] = set()
    for gf in goal_files:
        goal, err = load_card(gf, "goal")
        if err:
            continue
        for member in goal.get("members") or []:
            if not isinstance(member, dict):
                continue
            substance_id = member.get("substance")
            if isinstance(substance_id, str):
                refs.add(substance_id)
    return refs


def build_goal_review(
    *,
    goal_files: list[Path],
    active_substances: set[str],
    inactive_substances: set[str],
    substances: dict[str, dict],
) -> list[dict]:
    review: list[dict] = []
    for goal_file in goal_files:
        goal, err = load_card(goal_file, "goal")
        if err:
            continue
        taking_total = 0
        active_count = 0
        covered: list[str] = []
        inactive: list[str] = []
        missing: list[str] = []

        for member in goal.get("members") or []:
            if not isinstance(member, dict):
                continue
            if member.get("status") != "taking":
                continue
            substance_id = member.get("substance")
            if not isinstance(substance_id, str):
                continue
            taking_total += 1
            if substance_id in active_substances:
                active_count += 1
                covered.append(
                    format_substance_name(substances.get(substance_id) or {"id": substance_id})
                )
            elif substance_id in inactive_substances:
                inactive.append(
                    format_substance_name(substances.get(substance_id) or {"id": substance_id})
                )
            else:
                missing.append(
                    format_substance_name(substances.get(substance_id) or {"id": substance_id})
                )

        coverage_ratio = active_count / taking_total if taking_total else 0.0
        goal_entry = {
            "name": goal.get("name"),
            "coverage_percent": round(coverage_ratio * 100),
            "covered": sorted(covered, key=str.casefold),
        }
        if inactive:
            goal_entry["inactive"] = sorted(inactive, key=str.casefold)
        if missing:
            goal_entry["missing"] = sorted(missing, key=str.casefold)
        review.append(goal_entry)

    return review


def collect_orphans() -> dict[str, list[str]]:
    substance_files = sorted(SUBSTANCES_DIR.glob("*.yaml"))
    product_files = sorted(PRODUCTS_DIR.glob("*.yaml"))
    goal_files = sorted(GOALS_DIR.glob("*.yaml")) if GOALS_DIR.exists() else []

    substances: dict[str, dict] = {}
    for sf in substance_files:
        substance, err = load_substance(sf)
        if err:
            continue
        substance_id = substance.get("id")
        if isinstance(substance_id, str):
            substances[substance_id] = substance

    products: dict[str, dict] = {}
    product_substance_refs: set[str] = set()
    for pf in product_files:
        product, err = load_product(pf)
        if err:
            continue
        product_id = product.get("id")
        if isinstance(product_id, str):
            products[product_id] = product
        for component in product.get("components") or []:
            if not isinstance(component, dict):
                continue
            substance_id = component.get("substance")
            if isinstance(substance_id, str):
                product_substance_refs.add(substance_id)

    prefer_with_refs: set[str] = set()
    relation_refs: set[str] = set()
    trait_refs: set[str] = set()
    for substance_id, substance in substances.items():
        if substance.get("prefer_with"):
            prefer_with_refs.add(substance_id)
        for target_id in substance.get("prefer_with") or []:
            if isinstance(target_id, str):
                prefer_with_refs.add(target_id)
        for trait_id in substance.get("traits") or []:
            if isinstance(trait_id, str):
                trait_refs.add(trait_id)
    relation_refs.update(global_relation_refs(substances, load_global_relations()))

    traits_data = load_yaml(DATA_DIR / "traits.yaml")
    traits = traits_data.get("traits", {}) if isinstance(traits_data, dict) else {}
    for trait in traits.values():
        if not isinstance(trait, dict):
            continue
        for target_id in trait.get("separate_from") or []:
            if isinstance(target_id, str):
                trait_refs.add(target_id)

    inventory_data = load_yaml(INVENTORY_PATH)
    inventory_entries = (
        normalize_inventory_entries(inventory_data)
        if isinstance(inventory_data, dict)
        else {}
    )
    inventory_products = {
        entry["product"]
        for entry in inventory_entries.values()
        if isinstance(entry, dict) and isinstance(entry.get("product"), str)
    }
    active_inventory_products = {
        entry["product"]
        for entry in inventory_entries.values()
        if (
            isinstance(entry, dict)
            and entry.get("stack") != "inactive"
            and isinstance(entry.get("product"), str)
        )
    }
    active_substances = collect_product_substance_refs(
        products, active_inventory_products
    )
    inventory_stacks = (
        inventory_data.get("stacks", {}) if isinstance(inventory_data, dict) else {}
    )
    if not isinstance(inventory_stacks, dict):
        inventory_stacks = {}

    slots_data = load_yaml(DATA_DIR / "pillboxes.yaml")
    pillboxes = slots_data.get("pillboxes", {}) if isinstance(slots_data, dict) else {}
    pillbox_stacks = {
        pillbox.get("inventory_stack")
        for pillbox in pillboxes.values()
        if isinstance(pillbox, dict) and isinstance(pillbox.get("inventory_stack"), str)
    }

    substance_refs = (
        product_substance_refs
        | collect_goal_substance_refs(goal_files)
        | prefer_with_refs
        | relation_refs
    )
    unused_substances = sorted(set(substances) - substance_refs)
    products_without_inventory = sorted(set(products) - inventory_products)
    unused_traits = sorted(set(traits) - trait_refs)
    empty_stacks = sorted(
        stack
        for stack, items in inventory_stacks.items()
        if isinstance(items, list) and not items
    )
    stacks_without_pillboxes = sorted(
        set(inventory_stacks) - pillbox_stacks - {"inactive"}
    )
    pillbox_stacks_without_inventory = sorted(pillbox_stacks - set(inventory_stacks))

    return {
        "substances.unused": unused_substances,
        "products.without_inventory": products_without_inventory,
        "traits.unused": unused_traits,
        "stacks.empty": empty_stacks,
        "stacks.without_pillboxes": stacks_without_pillboxes,
        "pillbox_stacks.without_inventory": pillbox_stacks_without_inventory,
        "substances.similar_names": collect_similar_substances(substances),
        "relations.balance_missing": [
            format_relation_warning(warning)
            for warning in collect_missing_balance_relations(
                substances, active_substances, load_global_relations()
            )
        ],
        "relations.supports_missing": [
            format_relation_warning(warning)
            for warning in collect_missing_support_relations(
                substances, active_substances, load_global_relations()
            )
        ],
    }


def check_goals(goal_files: list[Path], substance_ids: dict[str, Path]) -> list[str]:
    """Validate goal cards against schema and members[].substance refs."""
    errors: list[str] = []
    for gf in goal_files:
        try:
            goal = load_yaml(gf)
        except yaml.YAMLError as e:
            errors.append(f"{gf}: yaml parse error: {e}")
            continue
        if goal is None:
            errors.append(f"{gf}: empty file")
            continue
        if not isinstance(goal, dict):
            errors.append(f"{gf}: top-level must be a mapping")
            continue

        errors.extend(schema_errors(goal, "goal", gf))
        for i, member in enumerate(goal.get("members") or []):
            if not isinstance(member, dict):
                continue
            ref = member.get("substance")
            if ref is None:
                continue
            if ref not in substance_ids:
                errors.append(
                    f"{gf}: members[{i}].substance '{ref}' has no matching substance card "
                    f"(expected at data/substances/{ref}.yaml)"
                )
    return errors


def report(errors: list[str], info: list[str]) -> int:
    for msg in info:
        print(f"INFO: {display_message(msg)}")
    if errors:
        for e in errors:
            print(f"ERROR: {display_message(e)}", file=sys.stderr)
        print(f"\n{len(errors)} error(s) found", file=sys.stderr)
        return 1
    print("All checks passed.")
    return 0


def validate_inventory(
    inventory_path: Path,
    product_ids: dict[str, Path],
    trait_ids: set[str],
) -> list[str]:
    if not inventory_path.exists():
        return [f"missing: {inventory_path}"]
    try:
        inventory_data = load_yaml(inventory_path)
    except yaml.YAMLError as e:
        return [f"{inventory_path}: yaml parse error: {e}"]
    if not isinstance(inventory_data, dict):
        return [f"{inventory_path}: top-level must be a mapping"]
    errors = schema_errors(inventory_data, "inventory", inventory_path)
    errors.extend(check_inventory_duplicate_items(inventory_data))
    errors.extend(check_inventory_alignment(inventory_data, product_ids))
    return errors


